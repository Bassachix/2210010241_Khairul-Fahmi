    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        // KODE TOMBOL CARI SUPPLIER
        try {
            
            String sql = "SELECT * FROM supplier WHERE kode_sup = '"+txtKodeSupplier.getText()+"'";
            Statement perintah = crud.Koneksi.koneksidb.createStatement();
            ResultSet data = perintah.executeQuery(sql);
            if (data.next()) {
                txtTelpon.setText(data.getString("nama_sup"));
                txtNamaSupplier.setText(data.getString("telpon"));
                txtAlamat.setText(data.getString("alamat"));
            } else {
                txtTelpon.setText(null);
                txtNamaSupplier.setText(null);
                txtAlamat.setText(null);
                JOptionPane.showMessageDialog(this, "Data yang Anda Cari Tidak Ditemukan!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi Error: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

