    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        // KODE TOMBOL SIMPAN SUPPLIER
        try {
            
            if(txtKodeSupplier.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Kode Supplier Belum Diisi!");
                txtKodeSupplier.requestFocus();
            } else if(txtNamaSupplier.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Nama Supplier Belum Diisi!");
                txtNamaSupplier.requestFocus();
            } else if(txtTelpon.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Telpon Belum Diisi!");
                txtTelpon.requestFocus();
            } else if(txtAlamat.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Alamat Belum Diisi!");
                txtAlamat.requestFocus();
            } else {
                int kodeSupplier = Integer.parseInt(txtKodeSupplier.getText());
                
                CRUD.simpanSupplier(kodeSupplier, txtNamaSupplier.getText(), txtTelpon.getText(), txtAlamat.getText());
                JOptionPane.showMessageDialog(this, "Supplier Berhasil Disimpan!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harap masukkan nilai numerik yang valid untuk Kode Supplier.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi Error: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        // KODE TOMBOL UBAH SUPPLIER
        try {
            
            if  (
                    txtKodeSupplier.getText().equals("") &&
                    txtNamaSupplier.getText().equals("") &&
                    txtTelpon.getText().equals("") &&
                    txtAlamat.getText().equals("")
                )      
            {
                JOptionPane.showMessageDialog(this, "Silahkan Cari Data Terlebih Dahulu!");
                txtKodeSupplier.requestFocus();
            } else {
                int kodeSupplier = Integer.parseInt(txtKodeSupplier.getText());
                
                CRUD.ubahSupplier(kodeSupplier, txtNamaSupplier.getText(), txtTelpon.getText(), txtAlamat.getText());
                JOptionPane.showMessageDialog(this, "Supplier Berhasil Diubah!");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harap masukkan nilai numerik yang valid untuk Kode Barang, Stok, dan Harga.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Supplier Gagal Diubah: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

